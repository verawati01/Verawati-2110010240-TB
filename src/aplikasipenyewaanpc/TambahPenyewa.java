     public void tampil_data(){
    DefaultTableModel tabel=new DefaultTableModel();
    tabel.addColumn("ID");
    tabel.addColumn("NAMA PENYEWA"); // untuk menampilkan title dibagian tabel dari data 
    tabel.addColumn("TANGGAL SEWA");
    tabel.addColumn("MEREK/TYPE");
    tabel.addColumn("LAMA SEWA");
    
    try {
        java.sql.Connection conn=(java.sql.Connection)aplikasipenyewaanpc.Koneksi.koneksiDB();
        String sql = "select * from tambahsewa";  // koneksi database dan memilih tabel sesuai dengan nama tabel
        java.sql.PreparedStatement pst = conn.prepareStatement(sql);
        ResultSet rs = pst.executeQuery(sql);
      while(rs.next())
        {
        tabel.addRow(new Object[]{
            rs.getString(1),
            rs.getString(2),
            rs.getString(3), //untuk manampilkan hasil tambah data 
            rs.getString(4),
            rs.getString(5)});
        }
        jTable1.setModel(tabel);
        }
    catch (Exception e){
    }
}
