                    +jTanggal.getText()+"','"
                    +jMerek.getText()+"','"
                    +jLama.getText()+"')";
                 java.sql.Connection conn=(java.sql.Connection)aplikasipenyewaanpc.Koneksi.koneksiDB();
            java.sql.PreparedStatement pst=conn.prepareStatement(sql); //untuk koneksi ke mysql 
            pst.execute();
            JOptionPane.showMessageDialog(null, "Berhasil disimpan");
            tampil_data(); //memanggil kelas untuk menampilkan hasil tambah data
